# Adição
print(10 + 5)

# Subtração
print(10 - 5)

# Multiplicação
print(10 * 5)

# Divisão
print(10 / 5)

# Divisão inteira
print(10 // 3)

# Módulo (resto da divisão)
print(10 % 3)

# Exponenciação (potência)
print(10 ** 3)

# Mais exemplos

print(10 - 5 * 2)

print((10 - 2) * 2)

print(10 ** 2 * 2)

print(10 ** (2 * 2))

print(10 / 2 * 4)

