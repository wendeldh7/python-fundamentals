# Operadores de associação
curso = "Curso de python"
frutas = ["laranja", "uva", "limão"]
saques = [1500, 100]

"Python" in curso # True  
# Verifica se a string "Python" está contida na variável curso

"maça" not in frutas # True
# Verifica se a string "maça" não está contida na lista de frutas

200 in saques # False
# Verifica se o valor 200 está contido na lista de saques

