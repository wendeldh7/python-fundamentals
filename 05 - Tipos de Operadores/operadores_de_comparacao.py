# Igualdade
balance = 450
withdraw = 200

print(balance == withdraw)  # False, porque 450 não é igual a 200

# Diferença (diferente)
balance = 450
withdraw = 200

print(balance != withdraw)  # True, porque 450 é diferente de 200

# Maior que / Maior ou igual a
balance = 450
withdraw = 200

print(balance > withdraw)   # True, porque 450 é maior que 200

print(balance >= withdraw)  # True, porque 450 é maior ou igual a 200

# Menor que / Menor ou igual a
balance = 450
withdraw = 200

print(balance < withdraw)   # False, porque 450 não é menor que 200

print(balance <= withdraw)  # False, porque 450 não é menor ou igual a 200

