# Igualdade
saldo = 450
saque = 200

print(saldo == saque)  # False, pois 450 não é igual a 200

# Diferença
saldo = 450
saque = 200

print(saldo != saque) # True, pois 450 é diferente de 200

# Maior que / Maior ou igual
saldo = 450
saque = 200

print(saldo > saque) # True, pois 450 é maior que 200

print(saldo >= saque) # True, pois 450 é maior ou igual a 200

# Menor que / Menor ou igual
saldo = 450
saque = 200

print(saldo < saque) # False, pois 450 não é menor que 200

print(saldo <= saque) # False, pois 450 não é menor ou igual a 200

