# Igualdade
saldo = 450
saque = 200

print(saldo == saque)  # False, porque 450 não é igual a 200

# Diferença (diferente)
saldo = 450
saque = 200

print(saldo != saque)  # True, porque 450 é diferente de 200

# Maior que / Maior ou igual a
saldo = 450
saque = 200

print(saldo > saque)    # True, porque 450 é maior que 200

print(saldo >= saque)   # True, porque 450 é maior ou igual a 200

# Menor que / Menor ou igual a
saldo = 450
saque = 200

print(saldo < saque)    # False, porque 450 não é menor que 200

print(saldo <= saque)   # False, porque 450 não é menor ou igual a 200

# Exercício para praticar os conceitos
