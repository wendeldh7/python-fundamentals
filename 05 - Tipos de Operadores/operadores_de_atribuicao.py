# Atribuição simples

saldo = 500
print(saldo) #500

# Atribuição com adição

saldo = 500
saldo += 200 # estou adicionando 200 ao saldo
print(saldo) #700

# Atribuição com subtração

saldo = 500
saldo -= 100 # estou subtraindo 100 do saldo
print(saldo) #400

# Atribuição com multiplicação

saldo = 500
saldo *=2 # estou multiplicando o saldo por 2
print(saldo) #1000

# Atribuição com divisão

saldo = 500
saldo /= 5 # estou dividindo o saldo por 5
print(saldo) #100.0

# Atribuição com divisão inteira

saldo = 500
saldo //= 5 # estou dividindo o saldo por 5 e pegando a parte inteira
print(saldo) #100

# Atribuição com módulo

saldo = 500
saldo %= 480 # estou pegando o resto da divisão do saldo por 480
print(saldo) #20

# Atribuição com exponenciação

saldo = 80
saldo **= 2 # estou elevando o saldo ao quadrado
print(saldo) #6400

