# Atribuição simples

balance = 500
print(balance)  # 500

# Atribuição com adição

balance = 500
balance += 200  # adicionando 200 ao saldo
print(balance)  # 700

# Atribuição com subtração

balance = 500
balance -= 100  # subtraindo 100 do saldo
print(balance)  # 400

# Atribuição com multiplicação

balance = 500
balance *= 2  # multiplicando o saldo por 2
print(balance)  # 1000

# Atribuição com divisão

balance = 500
balance /= 5  # dividindo o saldo por 5
print(balance)  # 100.0

# Atribuição com divisão inteira

balance = 500
balance //= 5  # dividindo o saldo por 5 e pegando a parte inteira
print(balance)  # 100

# Atribuição com módulo

balance = 500
balance %= 480  # pegando o resto da divisão do saldo por 480
print(balance)  # 20

# Atribuição com exponenciação

balance = 80
balance **= 2  # elevando o saldo à potência 2
print(balance)  # 6400

