# Herança simples e herança múltipla

# Herança simples

#  Quando uma classe filha herda apenas uma classe pai, ela é chamada deherança simples.

# Exemplo

class A:
    pass

class B(A):
    pass

# Herança múltipla

# Quando uma classe filha herda de várias classes pai, ela é chamada de herança múltipla.

# Exemplo

class A:
    pass

class B(A):
    pass

class C(A, B):
    pass
