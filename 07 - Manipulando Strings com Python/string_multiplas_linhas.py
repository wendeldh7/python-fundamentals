# Introdução

# Strings são sequências de caracteres, e podem ser manipuladas de várias formas em Python.

# Uma das formas de criar strings é utilizando aspas simples ou duplas, mas também podemos usar três aspas simples ou duplas para criar strings que ocupam várias linhas.

# Isso é útil quando queremos criar strings longas ou formatadas de maneira mais legível.

# Strings múltiplas linhas

nome = "Wendel"

mensagem = f"""Olá, meu nome é {nome},
Eu estou aprendendo Python.
""" # Essa string ocupa várias linhas e mantém a formatação.

