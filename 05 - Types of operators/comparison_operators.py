# Equality
balance = 450
withdraw = 200

print(balance == withdraw)  # False, because 450 is not equal to 200

# Difference (Not equal)
balance = 450
withdraw = 200

print(balance != withdraw)  # True, because 450 is different from 200

# Greater than / Greater than or equal to
balance = 450
withdraw = 200

print(balance > withdraw)  # True, because 450 is greater than 200

print(balance >= withdraw)  # True, because 450 is greater than or equal to 200

# Less than / Less than or equal to
balance = 450
withdraw = 200

print(balance < withdraw)  # False, because 450 is not less than 200

print(balance <= withdraw)  # False, because 450 is not less than or equal to 200

