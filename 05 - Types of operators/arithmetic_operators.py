# Addition
print(10 + 5)

# Subtraction
print(10 - 5)

# Multiplication
print(10 * 5)

# Division
print(10 / 5)

# Integer division
print(10 // 3)

# Modulo (remainder of the division)
print(10 % 3)

# Exponentiation (power)
print(10 ** 3)

# More examples

print(10 - 5 * 2)

print((10 - 2) * 2)

print(10 ** 2 * 2)

print(10 ** (2 * 2))

print(10 / 2 * 4)

