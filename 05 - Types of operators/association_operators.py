# Membership operators
course = "Python course"
fruits = ["orange", "grape", "lemon"]
withdrawals = [1500, 100]

"Python" in course  # True
# Checks if the string "Python" is contained in the variable course

"apple" not in fruits  # True
# Checks if the string "apple" is NOT contained in the fruits list

200 in withdrawals  # False
# Checks if the value 200 is contained in the withdrawals list

