# Estrutura Condicional Ternária

# A estrutura condicional ternária é uma forma compacta de escrever uma instrução if-else em Python.
# Ela permite atribuir um valor a uma variável com base em uma condição, tudo em uma única linha.

# Exemplo:

balance = 2000
withdraw = 1500

status = "Sucesso" if balance >= withdraw else "Falha"

print(f"{status} ao realizar o saque!")

# Exercício para praticar o conceito

balance = 2000
withdraw = 2500

status = "Sucesso" if balance >= withdraw else "Falha"

print(f"{status} ao realizar o saque!")
