# Estrutura Condicional Ternária

# A estrutura condicional ternária é uma forma compacta de escrever uma condição if-else em Python. Ela permite que você atribua um valor a uma variável com base em uma condição, tudo em uma única linha.

# Exemplo:

saldo = 2000
saque = 1500

status = "Sucesso" if saldo >= saque else "Falha"

print(f"{status} ao realizar o saque!")

# Exercício para fixação de conteúdo

